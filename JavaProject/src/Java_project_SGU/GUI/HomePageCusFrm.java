    public void showMenuBlock() {
        new Thread(new Runnable() {
            @Override
            public void run() {
                for (int i = 0; i <= 82; i++) {
                    try {
                        Thread.sleep(3);
                        blockMenuPN1.setSize(200, i);
                        blockMenuPN2.setSize(200, i);
                    } catch (InterruptedException ex) {
                        ex.printStackTrace();
                    }
                }
                isOpenMenu = true;
            }
        }).start();
    }
    
    
    public void closeMenuBlock() {
        new Thread(new Runnable() {
            @Override
            public void run() {
                for (int i = 82; i >= 0; i--) {
                    try {
                        Thread.sleep(3);
                        blockMenuPN1.setSize(200, i);
                        blockMenuPN2.setSize(200, i);
                    } catch (InterruptedException ex) {
                        ex.printStackTrace();
                    }
                }
                isOpenMenu = false;
            }
        }).start();
    }

