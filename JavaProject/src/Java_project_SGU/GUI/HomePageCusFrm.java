    private void loadListMovieToHomePage() {
        this.panelDisplayMovies.removeAll();
        this.panelDisplayMovies.setLayout(new FlowLayout(FlowLayout.CENTER, 50, 50));
        this.listPanelMovie = new ArrayList<>();
        this.listDeltaiMVLB = new ArrayList<>();
        
        Font myFont = new Font("Arial", Font.BOLD, 14);
        for (int i = 0; i < classListMovie.getMovies().size(); i++) {
            JPanel individualMoviePN = new JPanel(new FlowLayout(FlowLayout.CENTER, 10, 10));
            individualMoviePN.setBackground(new java.awt.Color(153, 0, 153));
//            individualMoviePN.setSize(260, 450);
             individualMoviePN.setPreferredSize(new Dimension(260, 464));
//            jPanel.setMinimumSize(jPanel.getPreferredSize());
//            jPanel.setMaximumSize(jPanel.getPreferredSize());
            individualMoviePN.setCursor(new Cursor(Cursor.HAND_CURSOR));
            
            JLabel movieNameLB = new JLabel("TÊN PHIM: " + classListMovie.getMovies()
                    .get(i).getNameMovie());
            movieNameLB.setFont(myFont);
            movieNameLB.setForeground(Color.WHITE);
            
            JLabel imageLB = new JLabel();
            imageLB.setSize(240, 355);
            imageLB.setIcon(new ImageIcon(classListMovie.
                    getMovies().get(i).getLinkImage()));
            
            JLabel typeOfMovie = new JLabel("Thể Loại: " + classListMovie.getMovies()
                    .get(i).getTypeMovie());
            typeOfMovie.setFont(myFont);
            typeOfMovie.setForeground(Color.WHITE);
            
            JLabel detailLB = new JLabel("Chi tiết phim");
            detailLB.setBackground(Color.red);
            detailLB.setFont(myFont);
            detailLB.setCursor(new Cursor(Cursor.HAND_CURSOR));
            detailLB.setOpaque(true);
            detailLB.setForeground(Color.WHITE);
            
            individualMoviePN.add(imageLB);
            individualMoviePN.add(movieNameLB);
            individualMoviePN.add(typeOfMovie);
            individualMoviePN.add(detailLB);
            
            listDeltaiMVLB.add(detailLB);
            listPanelMovie.add(individualMoviePN);
            this.panelDisplayMovies.add(listPanelMovie.get(i));
        }
        
        addNewAction();
        //Refresh page
        this.panelDisplayMovies.repaint();
        this.panelDisplayMovies.revalidate();
    }
          
